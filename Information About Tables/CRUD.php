O que é um CRUD?

C: Create - criar um novo registro;
R: Read - ler as informações de um registro;
U: Update - atualizar os dados de um registro;
D: Delete - apagar um registro;

O que quer dizer quando alguém fala: Vamos criar um CRUD!

Isso nada mais é do que: Terá uma operação de CADASTRO, uma operação de LEITURA dos usuários, uma operação de EXCLUSÃO dos usuários e uma operação de ATUALIZAÇÃO dos usuários.